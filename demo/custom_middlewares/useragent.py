import random

from scrapy.downloadermiddlewares.useragent import UserAgentMiddleware


class RotateUserAgentMiddleware(UserAgentMiddleware):
    def __init__(self, user_agent=''):
        super(RotateUserAgentMiddleware, self).__init__(user_agent)
        self.user_agent = user_agent

    def process_request(self, request, spider):
        if not getattr(spider, 'rotate_user_agent', None):
            spider.crawler.stats.inc_value("downloader/request_count/user_agent/default", 1, 0)
            request.headers.setdefault('User-Agent', self.user_agent)

        else:
            spider.crawler.stats.inc_value("downloader/request_count/user_agent/rotating", 1, 0)
            user_agent_pool = self.mobile_user_agents \
                if request.meta.get('mobile_user_agent') \
                else self.desktop_user_agents

            ua = random.choice(user_agent_pool)
            request.headers.setdefault('User-Agent', ua)

